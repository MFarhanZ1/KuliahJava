public class MengetahuiUmurDariTahunLahir{
	
	public static void main(String[] args){
		
		// mendeklarasikan suatu variabel yang berisi nilai tahun sekarang
		int tahunSekarang = 2022;
		
		// mendeklarasikan suatu variabel yang berisi nilai tahun lahir user
		int tahunLahir = 2004;
		
		// melakukan operasi matematika untuk mengetahui umur user
		int umur = tahunSekarang - tahunLahir;
		
		// mengeluarkan nilai umur sebagai output
		System.out.println("Karna kamu lahir tahun " + tahunLahir + ", maka umur mu sekarang adalah " + umur + " tahun!");

	}
	
}